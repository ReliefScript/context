local function win()
	for _, word in pairs(words) do
		task.spawn(function()
			guess:InvokeServer(word)
		end)
	end
end

local UI = getgenv().UI or loadstring(game:HttpGet("https://raw.githubusercontent.com/ReliefScript/mogware/refs/heads/main/main.lua"))()

local Window = UI:Window()
local Tab = Window:Tab("Game")
local Section = Tab:Section("Auto Guess")

Section:Toggle("Enabled", function(Toggled)
	if Toggled then
		R = true
		task.spawn(function()
			while R do
				win()

				local Started = false
				task.spawn(function()
					local C C = sync.OnClientEvent:Connect(function(tbl)
						if tbl.data.gameStateAtom.canGuess then
							Started = true
							C:Disconnect()
						end
					end)
				end)

				repeat task.wait() until Started
			end
		end)
	else
		R = false
	end
end)

Section:Button("Win", win)

Section:Button("Guess Random", function()
	guess:InvokeServer(words[math.random(#words)])
end)
